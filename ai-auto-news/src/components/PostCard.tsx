import Link from 'next/link';
import { Post } from '@/types';

function getReadingTime(content: string): number {
  const text = content.replace(/<[^>]*>/g, '');
  const words = text.split(/\s+/).filter(Boolean).length;
  return Math.max(1, Math.ceil(words / 200));
}

function getCategoryBadgeClass(category: string): string {
  const map: Record<string, string> = {
    blog: 'badge-blog',
    news: 'badge-news',
    tech: 'badge-tech',
    business: 'badge-business',
    sports: 'badge-sports',
    ai: 'badge-ai',
  };
  return map[category] || 'badge-blog';
}

function getCategoryEmoji(category: string): string {
  const map: Record<string, string> = {
    blog: '📝',
    news: '📰',
    tech: '💻',
    business: '📊',
    sports: '🏆',
    ai: '🧠',
  };
  return map[category] || '📄';
}

export default function PostCard({ post, index = 0 }: { post: Post; index?: number }) {
  const readingTime = getReadingTime(post.content);

  return (
    <article
      className="card-gradient animate-fade-in-up"
      style={{
        opacity: 0,
        animationDelay: `${index * 0.08}s`,
        animationFillMode: 'forwards',
      }}
    >
      <div
        className="p-6"
        style={{
          background: 'var(--bg-card)',
          borderRadius: 'var(--radius-lg)',
          transition: 'all 0.3s ease',
        }}
      >
        {/* Top badges */}
        <div className="flex items-center gap-2 mb-3 flex-wrap">
          <span className={`badge ${getCategoryBadgeClass(post.category)}`}>
            {getCategoryEmoji(post.category)} {post.category.charAt(0).toUpperCase() + post.category.slice(1)}
          </span>
          {post.autoGenerated && (
            <span className="badge badge-auto">
              🤖 Auto
            </span>
          )}
          <span
            className="ml-auto flex items-center gap-1"
            style={{ color: 'var(--text-muted)', fontSize: '0.75rem' }}
          >
            <svg className="h-3.5 w-3.5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
                d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
            </svg>
            {readingTime} min read
          </span>
        </div>

        {/* Title */}
        <Link href={`/post/${post.slug}`}>
          <h2
            className="text-lg font-semibold mb-2 transition-colors duration-200 line-clamp-2"
            style={{ color: 'var(--text-primary)' }}
          >
            {post.title}
          </h2>
        </Link>

        {/* Summary */}
        <p
          className="text-sm mb-4 line-clamp-3"
          style={{ color: 'var(--text-secondary)', lineHeight: '1.6' }}
        >
          {post.summary}
        </p>

        {/* Footer */}
        <div className="flex items-center justify-between">
          <div className="flex flex-wrap gap-1.5">
            {post.tags.slice(0, 3).map((tag) => (
              <span
                key={tag}
                className="px-2 py-0.5 rounded text-xs"
                style={{
                  background: 'rgba(255, 255, 255, 0.05)',
                  color: 'var(--text-muted)',
                  fontSize: '0.7rem',
                }}
              >
                #{tag}
              </span>
            ))}
          </div>
          <time
            className="text-xs"
            style={{ color: 'var(--text-muted)' }}
          >
            {new Date(post.createdAt).toLocaleDateString('en-US', {
              year: 'numeric',
              month: 'short',
              day: 'numeric',
            })}
          </time>
        </div>
      </div>
    </article>
  );
}
